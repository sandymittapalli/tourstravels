package SeleniumProject.SeleniumProject;

public class Inheritance {

	public void test1() {
		System.out.println("public");
	}

	private void test2() {
		System.out.println("private");
	}

	protected void test3() {
		System.out.println("protected");
	}

	void test4() {
		System.out.println("default");
	}

	
}
